# Sum of two floating point values
x = 25.72
print("type(x): ",type(x))

y = 102.84
print("type(y): ",type(y))


print("x+y =",x+y)
print("type(x+y): ",type(x+y))


# Difference between two integer values
x = 72
print("type(x): ",type(x))

y = 17
print("type(y): ",type(y))

print("x-y =",x-y)
print("type(x-y): ",type(x-y))


# Product of floating point and integer values
x = 116.25
print("type(x): ",type(x))

y = 72
print("type(y): ",type(y))

print("x*y =",x*y)
print("type(x*y): ",type(x*y))